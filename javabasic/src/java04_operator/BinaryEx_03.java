package java04_operator;

public class BinaryEx_03 {
	public static void main(String[] args){
		
		//이항연산자 - 관계, 비교
		
		// > gt, greater than
		// < lt, less than
		// >= ge, greater than equal
		// <= le, less than equal
		// == eq, equal
		// != ne, not equal
		
		// 두개의 피연산자의 크기를 비교하거나 관계를 따진다
		// 연산의 결과가 boolean타입이다
		
		int num1 = 10;
		int num2 = 20;
		
		System.out.println("num1==num2 : " + (num1==num2));
		System.out.println("num1!=num2 : " + (num1!=num2));
		System.out.println("num1>num2 : " + (num1>num2));
		System.out.println("num1<num2 : " + (num1<num2));
		System.out.println("num1>=num2 : " + (num1>=num2));
		System.out.println("num1<=num2 : " + (num1<=num2));
		

	}

}
