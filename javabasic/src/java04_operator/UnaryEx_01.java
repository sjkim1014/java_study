package java04_operator;

//단항연산자
//Unary Operator

public class UnaryEx_01 {
	public static void main(String[] args) {
		//단항연산자 - 부호
		// + -
		
		int num = 7;
		
		System.out.println(-num);
		System.out.println(-3);
		
		int num2 = -10;
		System.out.println(+num2);
		System.out.println(-num2);
		
		
	}
}
