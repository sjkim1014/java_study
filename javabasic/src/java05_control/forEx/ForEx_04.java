package java05_control.forEx;

public class ForEx_04 {
	public static void main(String[] args) {
		
		int sum=0;
		
		for(int i=1; i<=10; i++) {			
			sum += i;			
		}
		System.out.println(sum);
		
		//반복횟수 : 10
		//반복구간 : 1~10
		
		//초기식 i=1
		//조건식 i<=10, i<11
		//증감식 i++
	}
}
