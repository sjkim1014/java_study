package java05_control.forEx;

public class ForEx_05 {
	public static void main(String[] args) {
		
		//구구단 2단을 출력
		//출력 예시
		
		for(int i=1; i<=9; i++) {
			System.out.println("2 x " + i + " = " + (i*2));
		}
		
		//반복횟수 : 9
		//반복구간 : 1~9
		
		//초기식 : i=1
		//조건식 : i<=9
		//증감식 : i++
	}
}
